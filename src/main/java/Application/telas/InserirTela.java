    private void btnConfirmarInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarInserirActionPerformed
        //coletar o q foi digitado
        String nome = txtNomeInserir.getText();
        String cpf = txtCpfInserir.getText();
        String telefone = txtTelefoneInserir.getText();
        String endereco = "CEP: " + txtCepInserir.getText() + "\nCidade: " + txtCidadeInserir.getText() + "\nUF: " + txtUFInserir.getText() + "\nEndereço: " + txtRuaInserir.getText() + ", " + txtNumInserir.getText() + ", " + txtBairroInserir.getText() + "\n" + txtComplementoInserir.getText();
        String necessidades = txtNecessidadesInserir.getText();

        if(!txtCepInserir.getText().isEmpty()){
            if(ValidacaoInserir.validarInserir(nome, cpf, telefone, endereco, necessidades)){
                CRUDTela t = new CRUDTela();
                t.setVisible(true);
                this.dispose();
            }
        }else {
            JOptionPane.showMessageDialog(null, "Insira um CEP válido!", "Atenção", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnConfirmarInserirActionPerformed

