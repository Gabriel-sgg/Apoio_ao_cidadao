    private void txtCepInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtCepInserirActionPerformed
        //AQUI VAI TER A API PEGANDO O DADO DE CEP COLOCADO E SETANDO OS OUTROS TEXTOS:
        try{
            ApiCEP apiCEP = new ApiCEP();
            EnderecoDto enderecoDto = apiCEP.getEndereco(txtCepInserir.getText());
            txtCidadeInserir.setText(enderecoDto.getLocalidade());
            txtUFInserir.setText(enderecoDto.getUf());
        } catch (IOException | InterruptedException e){ //collapse
            JOptionPane.showMessageDialog(null, "Erro, verifique o CEP digitado.", "Atenção!", JOptionPane.ERROR_MESSAGE);
            throw new RuntimeException(e);
        }

    }//GEN-LAST:event_txtCepInserirActionPerformed

